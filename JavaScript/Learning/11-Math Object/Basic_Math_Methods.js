// Math.pow(x, y) returns x^y just like: x**y
let x = 4, y = 2;
console.log("x to the power y: ");
console.log(Math.pow(x, y));

// Math.sqrt(a) returns square root of a
let a = 81;
console.log("Square root of a: ");
console.log(Math.sqrt(a));

// Math.abs(b) returns absolute positive value of b
let b = -81.5;
console.log("Absolute of b: ");
console.log(Math.abs(b));

// Math.min() returns minimum value between list of arguments
console.log("Minimum Value: ");
console.log(Math.min(2, 0, 150, -1, -20));

// Math.max() returns maximum value between list of arguments
console.log("Maximum Value: ");
console.log(Math.max(2, 0, 150, -1, -20));
