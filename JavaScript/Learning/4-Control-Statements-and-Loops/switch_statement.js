let color = "red";

switch(color)
{
	case "red":
		console.log("The color is red");
		break;
	case "green":
		console.log("The color is green");
		break;
	case "blue":
		console.log("The color is blue");
		break;
	case "yellow":
		console.log("The color is yellow");
		break;
	default:
		console.log("The color is invalid");

}
