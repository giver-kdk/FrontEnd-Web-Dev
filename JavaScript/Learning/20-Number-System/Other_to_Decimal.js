// Binary, Octal and Hexadecimal numbers are preceeded by 0b, 0o and 0x repectively.

let binary = 0b1100;			// Actual Binary Number
let octal = 0o14;				// Actual Octal Number
let hexadecimal = 0xC;			// Actual Hexadecimal Number

// Here, "base" = 10 means decimal 
console.log(binary.toString(10));			// Binary to Decimal
console.log(octal.toString(10));			// Octal Decimal
console.log(hexadecimal.toString(10));		// Hexadecimal to Decimal

